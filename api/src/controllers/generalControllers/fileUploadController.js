async function getAllFilesForCoordinator(req, res) {
  const collectionNames = [
    "progressOne",
    "progressTwo",
    "finalSubmission",
    "corrections",
    "presentationAndDemos",
    "finalReport",
    "proposalsExtras",
    "progressOneExtras",
    "progressTwoExtras",
    "finalSubmissionExtras",
    "correctionsExtras",
    "presentationAndDemosExtras",
    "finalReportExtras",
  ];

  try {
    const token = req.headers.authorization;
    const decoded = jwt.verify(token, process.env.SECRET_TOKEN);
    const coordinatorProgram = decoded.coordinator_program;
    const programStudents = await Student.getAllStudentsByProgram(
      coordinatorProgram
    );
    const studentIds = programStudents.map((student) => student.user_id);
    const allFiles = [];
    await Promise.all(
      collectionNames.map(async (collectionName) => {
        const files = await FileSubmission.getAllFilesForCoordinator(
          collectionName
        );
        if (files.length) {
          const filteredFiles = files
            .filter((file) => studentIds.includes(file.studentId))
            .map((file) => ({
              ...file,
              studentName: programStudents.find(
                (student) => student.user_id === file.studentId
              ).username,
              matricCard: programStudents.find(
                (student) => student.user_id === file.studentId
              ).matricCard,
            }));

          allFiles.push(...filteredFiles);
        }
      })
    );
    return res.status(200).json(allFiles);
  } catch (error) {
    console.error("Error fetching all files for coordinator:", error);
    return res.status(500).json({ error: "Internal server error" });
  }
}

