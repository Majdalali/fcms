const FileSubmission = require("../../models/generalModels/fileSubmissionModel");
const Lecturer = require("../../models/usersModels/lecturerModel");
const Student = require("../../models/usersModels/studentModel");
const { createNotification } = require("./notificationsController");
const { checkDeadline } = require("./sessionController");

const jwt = require("jsonwebtoken");
const crypto = require("crypto");
const multer = require("multer");
const path = require("path");
const fs = require("fs/promises");

let randomFileName;

const storage = multer.diskStorage({
  destination: function (req, file, cb) {
    cb(null, "./uploads");
  },
  filename: function (req, file, cb) {
    const randomString = crypto.randomBytes(10).toString("hex");
    const fileExtension = path.extname(file.originalname);
    randomFileName = randomString + fileExtension; // Assign the generated name to the variable
    cb(null, randomFileName);
  },
});

const upload = multer({ storage: storage });

async function uploadFile(io, connectedUsers, req, res) {
  const token = req.headers.authorization;
  const { submissionType, projectType } = req.body; // Extract submissionType from req.body

  try {
    const submissionValidity = await checkDeadline(submissionType);

    if (submissionValidity.error) {
      return res.status(400).json({ error: submissionValidity.error });
    }

    if (!submissionValidity.isWithinDeadline) {
      return res
        .status(400)
        .json({ error: "Submission has been closed for this selection" });
    }

    const decoded = jwt.verify(token, process.env.SECRET_TOKEN);
    const studentId = decoded.user_id;
    const supervisorId = decoded.supervisor;
    const coSupervisorsIds = decoded.coSupervisors;
    const examinersIds = decoded.examiners;

    // Concatenate all IDs into a single array
    const studentLecturerIds = [
      supervisorId,
      ...coSupervisorsIds,
      ...examinersIds,
    ];
    const allowedSubmissionTypes = [
      "proposals",
      "progressOne",
      "progressTwo",
      "finalSubmission",
      "corrections",
      "presentationAndDemos",
      "finalReport",
      "proposalsExtras",
      "progressOneExtras",
      "progressTwoExtras",
      "finalSubmissionExtras",
      "correctionsExtras",
      "presentationAndDemosExtras",
      "finalReportExtras",
    ];
    // Check if the provided submissionType is in the allowed types array
    if (!allowedSubmissionTypes.includes(submissionType)) {
      return res.status(400).json({ error: "Invalid submission type" });
    }

    // Check if the deadline for the submissionType has passed

    let files = req.files; // Extract uploaded files

    // Save file info to the specified collection based on submissionType
    let fileSubmissionData;

    if (Array.isArray(files)) {
      // Handle multiple files for "others" submissionType
      fileSubmissionData = files.map((file) => ({
        originalName: file.originalname,
        autogeneratedName: file.filename,
        studentId,
        projectType,
        createdAt: new Date(),
        submissionType, // Include submissionType in the data
      }));
    } else {
      // Handle single file for other submissionTypes
      fileSubmissionData = [
        {
          originalName: files.originalname,
          autogeneratedName: files.filename,
          studentId,
          projectType,
          createdAt: new Date(),
          submissionType, // Include submissionType in the data
        },
      ];
    }

    // Call the saveDataSubmission function from your model with submissionType and fileSubmissionData
    await FileSubmission.saveDataSubmission(submissionType, fileSubmissionData);

    await createNotification({
      fromUser: studentId,
      message: "Student submitted a new file",
      toUsers: studentLecturerIds,
      creator: "Student",
      type: "Submission",
    });

    const usersSockets = studentLecturerIds
      .map((userId) => connectedUsers[userId])
      .filter(Boolean);

    if (usersSockets.length > 0) {
      io.to(usersSockets).emit("notification", {
        message: "Student submitted a new file",
        // Additional data if needed
        data: submissionType,
      });
    }

    return res
      .status(200)
      .json({ message: "File info uploaded successfully", fileSubmissionData });
  } catch (error) {
    console.error("Error uploading a file (fileUploadController): ", error);
    return res.status(500).json({ error: "Internal server error" });
  }
}

async function checkExistingSubmission(req, res) {
  const token = req.headers.authorization;
  const { submissionType, projectType } = req.body;

  try {
    const decoded = jwt.verify(token, process.env.SECRET_TOKEN);
    const studentId = decoded.user_id;
    const allowedSubmissionTypes = [
      "proposals",
      "progressOne",
      "progressTwo",
      "finalSubmission",
      "corrections",
      "presentationAndDemos",
      "finalReport",
      "proposalsExtras",
      "progressOneExtras",
      "progressTwoExtras",
      "finalSubmissionExtras",
      "correctionsExtras",
      "presentationAndDemosExtras",
      "finalReportExtras",
    ];
    if (!allowedSubmissionTypes.includes(submissionType)) {
      return res.status(400).json({ error: "Invalid submission type" });
    }
    if (submissionType.includes("Extras")) {
      // For "Extras" categories, skip the existingSubmission check
      return res
        .status(200)
        .json({ message: "No existing submission check required" });
    }

    const existingSubmission = await FileSubmission.checkExistingSubmission(
      submissionType,
      studentId,
      projectType
    );

    if (existingSubmission) {
      return res.status(400).json({
        message: "Submission already exists for this student and type",
      });
    }

    return res.status(200).json({ message: "No existing submission found" });
  } catch (error) {
    console.error(error);
    return res.status(500).json({ error: "Internal server error" });
  }
}

async function displayFile(req, res) {
  const { fileName } = req.params;
  const filePath = path.join("./uploads", fileName);

  try {
    // Read the file asynchronously
    const fileData = await fs.readFile(filePath);

    // Send the file data in the response
    res.end(fileData);
  } catch (error) {
    console.error(error);
    return res.status(404).json({ error: "File not found" });
  }
}

